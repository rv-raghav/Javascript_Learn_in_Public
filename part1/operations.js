// Comparison operators
let num1 = 3;
let num2 = 4;
let num3 = 6;

console.log(num1 == num2);        // false - loose equality
console.log(num1 != num2);        // true - loose inequality
console.log(num1 === num2);       // false - strict equality
console.log(num1 > num3);         // false - greater than
console.log(num1 < num3);         // true - less than
console.log(num1 >= num3);        // false - greater than or equal
console.log(num1 <= num3);        // true - less than or equal

