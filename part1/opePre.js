// Operator Precedence Examples

// Basic arithmetic with parentheses
let score = ((2 * (3 + 2)) - 1);  // Follows PEMDAS rule
console.log(score);                // Output: 9

// Additional examples showing precedence
let example1 = 2 + 3 * 4;         // Multiplication before addition
console.log(example1);            // Output: 14

let example2 = (2 + 3) * 4;       // Parentheses take precedence
console.log(example2);            // Output: 20

// Logical operator precedence
let result = 5 > 3 && 2 < 4;      // Comparison before logical AND
console.log(result);              // Output: true

